import type { FlatTokens } from './flatten.js';

export interface BlockOptions {
  selector: string;
  layer?: string;
  mediaQuery?: string;
  extraProps?: Record<string, string>;
}

function renderVars(tokens: FlatTokens, indent: string): string {
  return Object.entries(tokens)
    .map(([name, value]) => `${indent}${name}: ${value};`)
    .join('\n');
}

export function renderBlock(tokens: FlatTokens, options: BlockOptions): string {
  const { selector, layer, mediaQuery, extraProps } = options;
  const extras = extraProps
    ? Object.entries(extraProps)
        .map(([k, v]) => `    ${k}: ${v};`)
        .join('\n') + '\n'
    : '';
  const inner = extras + renderVars(tokens, '    ');
  const rule = `  ${selector} {\n${inner}\n  }`;
  const layered = layer ? `@layer ${layer} {\n${rule}\n}` : rule.replace(/^  /gm, '');
  if (!mediaQuery) {
    return layered;
  }
  return `@media ${mediaQuery} {\n${layered.replace(/^/gm, '  ')}\n}`;
}

export function renderFile(header: string, blocks: string[]): string {
  return `${header}\n\n${blocks.join('\n\n')}\n`;
}

